# conditional statements in python
# 1. if statement
age = 12
if age >= 18:
    print("You are eligible to vote.")

# 2. if-else statement
score = 90
if score >= 50:
    print("You have passed the exam.")
else:
    print("You have failed the exam.")


# 3. if-elif-else statement
marks = 65
if marks >= 90:
    print("Grade: A")
elif marks >= 80:
    print("Grade: B")
elif marks >= 70:
    print("Grade: C")
else:
    print("Grade: D")
